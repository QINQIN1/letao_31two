//一进页面，发送请求，查询是否登录过，没有登录，拦截到登录页
$.ajax({
    type:"get",
    url:"/employee/checkRootLogin",
    dataType:"json",
    success:function(info){
        console.log(info);
        if(info.error==400){
            location.href="login.html";
        }
        if(info.success){
            console.log('用户已登录过');
        }
    }
})